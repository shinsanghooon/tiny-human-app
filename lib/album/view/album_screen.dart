    void onScaleUpdate(ScaleUpdateDetails details) {
      // 제스처에 따라 그리드 수를 동적으로 조절
      endScale = details.scale;
    }

    void onScaleEnd(ScaleEndDetails details) {
      print('set State grid count');
      setState(() {
        if (endScale < 1) {
          gridCount += 1;
        } else {
          gridCount -= 1;
        }

        if (gridCount == 0) {
          gridCount = 1;
        }

        if (gridCount == 11) {
          gridCount = 10;
        }
      });
    }

    return Scaffold(
      body: GestureDetector(
        onScaleUpdate: onScaleUpdate,
        onScaleEnd: onScaleEnd,
        child: CustomScrollView(
          slivers: [
            const SliverAppBar(
              backgroundColor: Colors.transparent,
              title: Text(
                "tiny-human",
                style: TextStyle(
                  color: Colors.deepOrange,
                  fontWeight: FontWeight.w800,
                ),
              ),
            ),
            SliverGrid(
              delegate: SliverChildBuilderDelegate((context, index) {
                if (index + 1 > imageUrls.length) {
                  return null;
                }
                return Padding(
                  padding: EdgeInsets.all(8 / gridCount),
                  child: GestureDetector(
                    onTap: () {
                      Navigator.push(
                        context,
                        MaterialPageRoute(
                          builder: (context) => PhotoRoute(
                            image: imageUrls[index],
                          ),
                        ),
                      );
                    },
                    child: Container(
                      decoration: BoxDecoration(
                        image: DecorationImage(
                          fit: BoxFit.cover,
                          image: NetworkImage(imageUrls[index]),
                        ),
                        borderRadius: BorderRadius.circular(6.0),
                      ),
                    ),
                  ),
                );
              }),
              gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
                crossAxisCount: gridCount.toInt(),
              ),
            )
          ],
        ),
      ),
    );
  }
}

class PhotoRoute extends StatelessWidget {
  final image;

  const PhotoRoute({
    required this.image,
    super.key,
  });

  @override
  Widget build(BuildContext context) {
    return InteractiveViewer(
      child: Scaffold(
        body: Center(
          child: Image.network(image),
        ),
      ),
    );
  }
}
